Creme de Pentear Encrespando A Juba:

Aplique o creme em mechas finas com cabelos úmidos, começando pela nuca e subindo. Deixe secar naturalmente ou use um difusor para manter a definição dos cachos.

Finalizador Juba Blend De Oleos Vegetais

Como umectante: Aplique uma quantidade generosa do Juba Blend de Óleos Vegetais nos cabelos secos e deixe agir por algumas horas antes de lavá-los. Siga com os produtos da linha Juba para um tratamento completo.

Como finalizador: Aplique uma pequena quantidade do Juba Blend de Óleos Vegetais nos cabelos úmidos ou secos como toque final para dar brilho e hidratação.

Método LOC (Líquido, Óleo, Creme): Após lavar e tratar os cabelos, divida-os em mechas. Com os fios bem molhados, aplique uma pequena quantidade do Juba Blend de Óleos Vegetais nas mãos e espalhe pelos fios. Continue a finalização aplicando o seu creme de pentear Juba favorito, fazendo a técnica de fitagem para uma melhor definição dos cachos.

